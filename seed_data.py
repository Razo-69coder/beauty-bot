async def seed():
    async with aiosqlite.connect(DB_PATH) as db:
        # Находим первого мастера
        async with db.execute("SELECT id FROM masters LIMIT 1") as cursor:
            row = await cursor.fetchone()
            if not row:
                print("❌ Мастер не найден. Сначала нажми /start в боте!")
                return
            master_id = row[0]
        print(f"✅ Мастер найден (id={master_id})")

        # Добавляем клиентов
        client_ids = []
        for name, phone, notes in CLIENTS:
            await db.execute(
                "INSERT INTO clients (master_id, name, phone, notes) VALUES (?, ?, ?, ?)",
                (master_id, name, phone, notes)
            )
            await db.commit()
            async with db.execute("SELECT last_insert_rowid()") as c:
                client_ids.append((await c.fetchone())[0])

        print(f"✅ Добавлено {len(client_ids)} клиентов")

        # Добавляем историю процедур
        total_appts = 0
        for i, history in enumerate(HISTORY):
            client_id = client_ids[i]
            for proc_idx, days in history:
                proc_name, price = PROCEDURES[proc_idx]
                await db.execute(
                    "INSERT INTO appointments (client_id, master_id, procedure, appointment_date, price) "
                    "VALUES (?, ?, ?, ?, ?)",
                    (client_id, master_id, proc_name, days_ago(days), price)
                )
                total_appts += 1
            await db.commit()

        print(f"✅ Добавлено {total_appts} записей процедур")
        print()
        print("🎉 База заполнена! Можно тестировать бот.")
        print()
        print("Что проверить:")
        print("  👥 Мои клиенты       — 15 клиентов, пагинация (стр. 1/2)")
        print("  🔍 Найти клиента     — попробуй 'Ан' или 'Юл'")
        print("  🔔 Давно не приходили — 5 клиентов (55–90 дней)")
        print("  📊 Статистика        — выручка, топ процедур")
        print("  📥 Экспорт в Excel   — скачает beauty_clients.xlsx")


asyncio.run(seed())
